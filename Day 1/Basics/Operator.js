console.log("Operators!!");
// Operators
let a1 = 80;
let b1 = 100;

// Addition Operator
console.log(a1 + b1);
// Adds the two values

// Subtraction Operator
console.log(a1 - b1);
// Subtracts b1 from a1

// Multiplication Operator
console.log(a1 * b1);
// Multiplies a1 with b1

// Division Operator
console.log(a1 / b1);
// Divides a1 by b1

// Modulus Operator (Remainder)
console.log(a1 % b1);
// Returns the remainder of a1 divided by b1

// Exponentiation Operator (Power)
console.log(a1 ** 2);
// Raises a1 to the power of 2 (a1 * a1)
