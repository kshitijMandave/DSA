console.log("Relational Operators!!");

// Equal To (==) : checks only value
/* 11 == "11"= true here  */
console.log(a1 == b1);

// Strict Equal To : checks value & type
/* 11 === "11" = false here */
console.log(a1 === b1);

// Not Equal (!=)
console.log(a1 != b1);

// Greater Than (>)
console.log(a1 > b1);

// Less Than (<)
console.log(a1 < b1);

// Greater Than or Equal To (>=)
console.log(a1 >= b1);

// Less Than or Equal To (<=)
console.log(a1 <= b1);
